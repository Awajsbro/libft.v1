#include "../libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t i;

	i = -1;
	while (++i < n && ((unsigned char*)src)[i] != ((unsigned char)c))
		((unsigned char*)dst)[i] = ((unsigned char*)src)[i];
	if (i == n)
		return (NULL);
	((unsigned char*)dst)[i] = ((unsigned char*)src)[i];
	return (&dst[i + 1]);
}
