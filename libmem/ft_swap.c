#include "../libft.h"

void	ft_swap(void **ptr1, void **ptr2)
{
	void	*tmp;

	if (ptr1 == ptr2)
		return ;
	tmp = *ptr1;
	*ptr1 = *ptr2;
	*ptr2 = tmp;
}
