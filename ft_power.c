#include "libft.h"

int	ft_power(int nb, int p)
{
	int i;
	int r;

	if (nb < 1 || p < 0)
		return (0);
	i = -1;
	r = 1;
	while (++i < p)
	{
		r = r * nb;
	}
	return (r);
}
