#include "libpt.h"

void	ft_ptdel(t_pt **pt)
{
	ft_strdel(&((*pt)->name));
	(*pt)->x = 0;
	(*pt)->y = 0;
	(*pt)->z = 0;
	free(*pt);
	*pt == NULL;
}
